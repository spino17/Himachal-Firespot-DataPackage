public class AboutInfo {

    private String district;
    private String about;

    public AboutInfo(String district, String about) {
        //
        this.district = district;
        this.about = about;
    }

    public String getAbout() {
        return about;
    }

    public String getDistrict() {

        return district;
    }
}
